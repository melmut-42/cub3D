#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	unsigned char	ch;
	int				i;

	i = 0;
	ch = (unsigned char)c;
	if (ch == '\0')
	{
		i = ft_strlen(str);
		return ((char *)str + i);
	}
	while (str[i])
	{
		if (str[i] == ch)
			return ((char *)str + i);
		i++;
	}
	return (NULL);
}
